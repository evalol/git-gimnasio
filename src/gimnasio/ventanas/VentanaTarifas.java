    public boolean edadesTarifa() {

        if (Integer.parseInt(txNuevoEdadMinima.getText()) > Integer.parseInt(txNuevoEdadMinima.getText())) {
            JOptionPane.showMessageDialog(this, "La edad mínima no puede ser superior a la máxima");
            return false;
        }
        return true;
    }

    public void insertarDatos() {

        try {
            Tarifas tarifa = new Tarifas(txNuevoNombreTarifa.getText(),
                    Integer.parseInt(txNuevoEdadMaxima.getText()),
                    Integer.parseInt(txNuevoEdadMinima.getText()),
                    Integer.parseInt(txNuevoCuota.getText()));

            GestoraTarifas.guardarTarifa(tarifa);
            tablaTarifas.setModel(new ModeloTablaTarifas(GestoraTarifas.recuperarTarifas()));
            JOptionPane.showMessageDialog(this, "Se ha insertado la tarifa.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se ha podido insertar la tarifa.");
        }

    }

    public void comprobarNumerico(JTextField campo, java.awt.event.KeyEvent evt) {

        char car = evt.getKeyChar();
        if (((car < '0' || car > '9') && car != '.') || campo.getText().contains(".") && car == '.') {
            evt.consume();
        }

    }

    private void buscar() {
        
        String buscar = txBusqueda.getText();
        int buscarN = 0;
        if(Patrones.isNumeric(buscar))
            buscarN = Integer.parseInt(buscar);
        if (!buscar.isEmpty()) {

            Session s = Gestora.getInstance().openSession();
            Query q = s.createQuery("from Tarifas t where t.nombreTarifa = :n or "
                            + "t.edadMinimaTarifa = :min or "
                            + "t.edadMaximaTarifa = :max or "
                            + "t.precioTarifa = :p");
            q.setParameter("n", buscar);
            q.setParameter("min", buscarN);
            q.setParameter("max", buscarN);
            q.setParameter("p", buscarN);
            tablaBusquedas.setModel(new ModeloTablaTarifas(q.list()));
        }
    }
